document.addEventListener('DOMContentLoaded', () => {
    const modalContainer = document.getElementById('modal-container');

    // --- LÓGICA PARA A PÁGINA DE PROGRAMAÇÃO ---
    const dayButtons = document.querySelectorAll('.day-button');
    const eventsContainer = document.querySelector('.events-list');

    if (dayButtons.length > 0 && eventsContainer && modalContainer) {
        function updateEventsList(day) {
            const events = scheduleData[day] || [];
            let cardsHTML = '';
            let modalsHTML = '';

            events.forEach((event, index) => {
                const uniqueId = `${day}-${index}`;
                cardsHTML += createEventCard(event, uniqueId);
                modalsHTML += createEventModal(event, uniqueId);
            });

            eventsContainer.innerHTML = cardsHTML;
            modalContainer.innerHTML = modalsHTML;
        }

        dayButtons.forEach(button => {
            button.addEventListener('click', () => {
                dayButtons.forEach(btn => btn.classList.remove('active'));
                button.classList.add('active');
                updateEventsList(button.textContent.trim());
            });
        });

        const activeButton = document.querySelector('.day-button.active') || dayButtons[0];
        if (activeButton) {
            activeButton.classList.add('active');
            updateEventsList(activeButton.textContent.trim());
        }
    }

    // --- LÓGICA PARA A PÁGINA DE OFICINAS ---
    const workshopsContainer = document.getElementById('workshops-cards');
    if (workshopsContainer && modalContainer) {
        function createWorkshopCards() {
            let cardsHTML = '';
            let modalsHTML = '';

            workshops.forEach((workshop, index) => {
                const uniqueId = `workshop-${index}`;
                cardsHTML += createEventCard(workshop, uniqueId);
                modalsHTML += createEventModal(workshop, uniqueId);
            });

            workshopsContainer.innerHTML = cardsHTML;
            modalContainer.innerHTML += modalsHTML; // Usamos += para não sobrescrever os modais da programação (se houver)
        }
        createWorkshopCards();
    }
});
